package Day09;

public class MaxNumber {
    public static void main(String[] args) {
        int num1 = 20,
                num2 = 30;
        if(num1 > num2){
            System.out.println(num1+" is max");
        }
        if (num2>num1){
            System.out.println(num2+" is max");
        }
    }
}
