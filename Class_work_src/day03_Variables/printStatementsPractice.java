package day03_Variables;

public class printStatementsPractice {
    public static void main(String[] args) {
        System.out.print("hello");// does not append new line
        System.out.print("Hola");// does not append new line

        System.out.println("How are you?");// appends new line
        System.out.print("Today is Friday");// does not append new line
        System.out.println("Tomorrow is Saturday");

        System.out.println("------------------------------------------");
        System.out.println(" \tHello Batch 23 \n   How are you today"); // paragraph space
        System.out.println("My name is \"Nathan\"");// Helps to put things in quotation
        System.out.println("My name is 'name' ");
    }
}
