package Day21Loops;

public class multiply2numbers {
    public static void main(String[] args) {
        int a = 10,
                b = 5;
        int result = 0;
        for (int i = 0; i < b; i++) {
             result += a;
            System.out.println("result:"+ result);
        }
    }
}
